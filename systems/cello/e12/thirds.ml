let make_major_scale ~from =
  let t = Lazy.force E12.t in
  System.Double_stops.make_scale
    t
    ~characterized_scale:Characterized_scale.major_e12
    ~interval_number:Third
    ~from
    ~to_:Cello.fingerboard_highest_note
;;

