let create () = Cello.fifth_system ()

let add_positions t =
  List.iter
    (Cello.Fingerboard_position_name.Pythagorean.all
      :> Cello.Fingerboard_position_name.t list)
    ~f:(fun name -> Cello.add_fingerboard_position_exn t name)
;;

let t =
  lazy
    (let t = create () in
     add_positions t;
     t)
;;

